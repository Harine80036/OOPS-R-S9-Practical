package Operators;

public class ShiftOperators {

	public static void main(String[] args) {
		 int num = 8;  // binary: 1000

	       
	        int leftShift = num << 2;  // Shifting 2 positions to the left
	        System.out.println("Left Shift (8 << 2): " + leftShift);

	        
	        int rightShift = num >> 2;  // Shifting 2 positions to the right
	        System.out.println("Right Shift (8 >> 2): " + rightShift);
		

	}

}


        
