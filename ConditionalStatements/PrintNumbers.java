package ConditionalStatements;

public class PrintNumbers {

	public static void main(String[] args) {
		int N = 5;  
        System.out.print("Numbers from 1 to " + N + ": ");
        for (int i = 1; i <= N; i++) {
            System.out.print(i + " ");
        }
        System.out.println();

	}

}
