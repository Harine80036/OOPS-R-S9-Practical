package ConditionalStatements;

public class EvenOrOdd {

	public static void main(String[] args) {
		int num2 = 4;  // Hardcoded number
        if (num2 % 2 == 0) {
            System.out.println(num2 + " is even.");
        } else {
            System.out.println(num2 + " is odd.");
        }

	}

}
