package ConditionalStatements;

public class MultiplicationTable {

	public static void main(String[] args) {
		 int tableNum = 4;  
	        System.out.println("Multiplication table for " + tableNum + ":");
	        for (int i = 1; i <= 10; i++) {
	            System.out.println(tableNum + " x " + i + " = " + (tableNum * i));
	        }
	    }
	}



